#include<stdio.h>

int main() {
    int a[4] = {192, 168, 1, 42};
    int b[4] = {192, 168, 2, 208};
    int c[4] = {255, 255, 255, 0};
    for (int i = 0; i < 4; i++) {
        printf("a[%d] = %d, b[%d] = %d, c[%d] = %d, a[%d] & c[%d] = %d, b[%d] & c[%d] = %d\n",i, a[i],i, b[i],i, c[i], i, i, (a[i]&c[i]), i, i, (b[i]&c[i]));
    }
    if ((a[0]&c[0])==(b[0]&c[0]) && (a[1]&c[1])==(b[1]&c[1]) && (a[2]&c[2])==(b[2]&c[2]) && (a[3]&c[3])==(b[3]&c[3])) {
        printf("Yes\n");
    } else {
        printf("No");
    }
    return 0;
}
